public class SelectionStatementsTask03 {

    public static void main(String[] args) {

        int num1 = 26;
        int num2 = 26;

        if (num1 > num2) {
            System.out.println(num1 + " is bigger than " + num2);
        } else if (num1 < num2) {
            System.out.println(num1 + " is less than " + num2);
        }else {
            System.out.println(num1 + " is equal to " + num2);
        }
    }
}
