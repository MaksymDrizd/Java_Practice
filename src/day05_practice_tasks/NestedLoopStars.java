package day05_practice_tasks;

public class NestedLoopStars {

    public static void main(String[] args) {


        for (int i = 1; i <= 6; i++) {

            for (int j = 1; j <= 6; j++) {
                System.out.print("*  ");
                
            }
            System.out.println();
        }
    }
}
