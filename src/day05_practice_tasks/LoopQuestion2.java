package day05_practice_tasks;

public class LoopQuestion2 {

    public static void main(String[] args) {

        for (int i = 0; i < 10; i++) {
            i++;
            System.out.println("Hello World"); // printed 5 times
        }
    }    // 0 + 1 = 1 1+ 1+   Print
          // 3  1+ 1+    Print
}         // 5  1+ 1+   Print
         // 7 1+ 1+     Print
         // 9 1+ 1+     Print
