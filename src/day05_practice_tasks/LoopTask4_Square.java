package day05_practice_tasks;

public class LoopTask4_Square {

    public static void main(String[] args) {

        System.out.println(" Number |  Square Number");
        System.out.println("--------------------------");


        for (int i = 1; i <= 10; i++) {
            int square = i * i;
            System.out.println("\t" + i + "\t|\t" + square);
        }

    }
}
