package day05_practice_tasks.m_16_17_18_LoopsPracticeTasks;

public class FactorialNumber {

    public static void main(String[] args) {

        int num = 10,
                factorial = 1;

        for (int i = 1; i <= num; i++) {
            factorial *= i;

        }System.out.println(factorial);

    }
}









