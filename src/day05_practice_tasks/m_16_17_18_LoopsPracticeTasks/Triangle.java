package day05_practice_tasks.m_16_17_18_LoopsPracticeTasks;

public class Triangle {

    public static void main(String[] args) {

        for (int i = 1; i < 10; i++) {
            for (int j = 1; j <= i; j++) {
                System.out.print(" * ");
            }
            System.out.println();
        }
    }
}
