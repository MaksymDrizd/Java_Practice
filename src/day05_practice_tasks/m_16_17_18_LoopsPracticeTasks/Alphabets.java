package day05_practice_tasks.m_16_17_18_LoopsPracticeTasks;

public class Alphabets {

    public static void main(String[] args) {

        for (char i = 'A'; i <= 'E'; i++) {
            for (char j = 'a'; j <= 'z'; j++) {
                System.out.print(i + "" + j + " ");
            }
            System.out.println();
        }
    }
}
